package dayOne.DataTypeandOperators;

public class Literals {

	public static void main(String[] args) {
		int a = 101;
		int b = 0100;
		int c = 0xFABA;
		int d = 0b1111;
		double e = 01234.123;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}
}
