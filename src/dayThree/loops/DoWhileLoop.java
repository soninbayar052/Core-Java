package dayThree.loops;

public class DoWhileLoop {
	public static void main(String[] args) {
		int i=10;
		do {
			System.out.println(i);
			i--;
		} while(i>1);
			
			
			
			
		int arr[]= {2,4,5,6,7,8,9,0};
		
		int j=0;
		do { 
			System.out.println(arr[j]);
			j++;
		}while (j <arr.length);
		
	}

}
