package dayFive.OOP2;

public class Address {
	
	String streetName;
	String city;
	String state;
	String country;
	public Address(String streetName, String city, String state, String country) {
		super();
		this.streetName = streetName;
		this.city = city;
		this.state = state;
		this.country = country;
	}
	

}
